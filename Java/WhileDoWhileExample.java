public class WhileDoWhileExample {
    public static void main(String[] args) {
        // While loop
        int i = 1;
        System.out.println("While Loop:");
        while (i <= 5) {
            System.out.println(i);
            i++;
        }

        // Do-while loop
        int j = 1;
        System.out.println("Do-While Loop:");
        do {
            System.out.println(j);
            j++;
        } while (j <= 5);
    }
}
